export function getCharacterPaths(char: string): Point[][] {
	return CHARACTER_PATHS[char.toUpperCase()] || [];
}

export function getTextPaths(
	text: string,
	startX: number,
	startY: number,
	scale: number = 1,
	spacing: number = 25,
): Point[][] {
	const paths: Point[][] = [];
	let currentX = startX;

	for (const char of text) {
		const charPaths = getCharacterPaths(char);

		for (const path of charPaths) {
			const scaledPath = path.map((point) => ({
				x: currentX + point.x * scale,
				y: startY + point.y * scale,
			}));

			if (scaledPath.length > 0) {
				paths.push(scaledPath);
			}
		}

		// Move to next character position
		currentX += spacing * scale;
	}

	return paths;
}
